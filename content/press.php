
Waschechte "Eigengewächse" sind die Vier von der Gruppe "Non Tacet" mit ihrem Programm "Percussion Night", die sich am 25. Juni die Ehre geben werden: Marcel Hug, Christoph Steiert, Lucas Grammelspacher und Markus Schneider, als Schlagzeuger in den Trachtenkapellen Bollschweil und St. Ulrich aufgewachsen. Inzwischen haben sie ihren eigenen Stil entwickelt und begeistern mit origineller Handhabung der verschiedensten Rhythmusinstrumente, kommen aber gelegentlich auch ganz ohne aus.

