
<p>
“Für uns ist Rhythmus die Basis für jedes gemeinsame Zusammenspiel. 
Ohne ein gemeinsames Rhythmusgefühl würde ein Zusammenspiel in 
egal welcher Form nicht funktionierten.” 
</p> 
<p>
Das Percussion Ensemble Nontacet wurde 2012 von befreundeten Schlagzeugern 
gegründet, um in erster Linie mit viel Spass zusammen Musik zu machen. 
 </p>
<p>
Zu Beginn haben sich die Musiker überwiegend auf traditionelle Percussion Instrumente 
wie Drumset, Xylophon, Marimbaphone, Pauken etc fokusiert. 
Im Laufe der Zeit entdeckten sie weitere, ungewöhnliche Möglichkeiten mit 
Percussion zu unterhalten. 
So ist zum Beispiel ein Stück mit Basketbällen ein stetiger Begleiter des Programms. 
Eine Auswahl verwendeter Instrumente ist: Leitern, Umzugskartons, Feuerzeuge, 
Gläser... 
 </p>
<p>
Ein Höhepunkt war die Mitgestaltung und Organisation von Wein & Percussion. 
Bei diesem Event wurde die Vorstellung von Weinen aus dem Weingut Mangold mit 
Musikeinlagen von Nontacet vervollständigt. 
 </p>
<p>
Durch Projekte mit Chören und Solokünstler kamen weitere Möglichkeiten des 
Zusammenspiels zustande.  
 </p>
<p>
Durch regelmäßige Auftritte seit 2012 wurde das Interesse der Musikzeitschrift 
“blasmusik” geweckt und im Themenheft “Aller Anfang ist Rhythmus” vom Juni 2017 über 
Nontacet berichtet.
</p>