    <p>
        « Pour nous, le rythme est la base d‘un ensemble harmonieux. Sans cela, un ensemble, quel qu’il soit, ne peut pas fonctionner. »
    </p>
    <p>
        L’ensemble de percussions Nontacet fut fondé en 2012 par un groupe d’amis musiciens animés par un but commun: le plaisir de jouer.
    </p>
    <p>
        Dans les premiers temps, le groupe s’est focalisé sur les instruments traditionnels tels que batterie, xylophone, marimbaphone, timbale…
    Par la suite, il a découvert d’autres moyens de divertir son public, grâce à des instruments originaux.
    A titre d’exemple, un morceau est joué à l’aide de ballons de basket. D’autres instruments, tout aussi inattendus, sont utilisés: des échelles, des cartons, des briquets, des verres…
    </p>
    <p>
        L’un des évènements qui ont marqué l’histoire du groupe a été la fête „Wein & Percussion“, au cours de laquelle les vins du producteur Mangold ont été présentés avec un accompagnement sonore de Nontacet.
    </p>
    <p>
        Nous avons aussi eu la chance d’accompagner diverse chorales et solistes.
    </p>
    <p>
        Nos représentations régulières ont attiré l’attention du magazine spécialisé « blasmusik » qui nous a consacré un article dans son édition “Aller Anfang ist Rhythmus” (tout débute par le rythme) de juin 2017.
    </p>
